package Model;

public class CursoComplementar {
    private String cursoCursoComplementar;
    private String duracaoCursoComplementar;
    private String instituicaoCursoComplementar;

    public String getCursoCursoComplementar() {
        return cursoCursoComplementar;
    }

    public void setCursoCursoComplementar(String cursoCursoComplementar) {
        this.cursoCursoComplementar = cursoCursoComplementar;
    }

    public String getDuracaoCursoComplementar() {
        return duracaoCursoComplementar;
    }

    public void setDuracaoCursoComplementar(String duracaoCursoComplementar) {
        this.duracaoCursoComplementar = duracaoCursoComplementar;
    }

    public String getInstituicaoCursoComplementar() {
        return instituicaoCursoComplementar;
    }

    public void setInstituicaoCursoComplementar(String instituicaoCursoComplementar) {
        this.instituicaoCursoComplementar = instituicaoCursoComplementar;
    }   
    
}
